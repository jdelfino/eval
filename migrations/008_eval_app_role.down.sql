-- Revoke privileges and drop the eval_app role.
-- Revoke default privileges first, then existing grants, then drop.

ALTER DEFAULT PRIVILEGES IN SCHEMA public REVOKE SELECT, INSERT, UPDATE, DELETE ON TABLES FROM eval_app;
ALTER DEFAULT PRIVILEGES IN SCHEMA public REVOKE USAGE ON SEQUENCES FROM eval_app;
ALTER DEFAULT PRIVILEGES IN SCHEMA public REVOKE EXECUTE ON FUNCTIONS FROM eval_app;

REVOKE EXECUTE ON ALL FUNCTIONS IN SCHEMA public FROM eval_app;
REVOKE USAGE ON ALL SEQUENCES IN SCHEMA public FROM eval_app;
REVOKE SELECT, INSERT, UPDATE, DELETE ON ALL TABLES IN SCHEMA public FROM eval_app;
REVOKE USAGE ON SCHEMA public FROM eval_app;
DO $$
BEGIN
  EXECUTE format('REVOKE CONNECT ON DATABASE %I FROM eval_app', current_database());
END
$$;

DROP ROLE IF EXISTS eval_app;
